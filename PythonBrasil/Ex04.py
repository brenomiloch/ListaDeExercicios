#Faça um Programa que peça as 4 notas bimestrais e mostre a média.

NotaBi_1 = int(input("Digite a primeira nota bimestral: "))
NotaBi_2 = int(input("Digite a segunda nota bimestral: "))
NotaBi_3 = int(input("Digite a terceira nota bimestral: "))
NotaBi_4 = int(input("Digite a quarta nota bimestral: "))

Media = (NotaBi_1 + NotaBi_2 + NotaBi_3 + NotaBi_4)/4

print("A media foi de: ", Media)