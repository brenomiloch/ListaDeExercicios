# Escreva um programa que receba um número inteiro do usuário e verifique se ele é par ou ímpar utilizando o operador
# de divisão inteira (%). Imprima na tela se o número é par ou ímpar.
num_01 = int(input("Digite um número: "))

if num_01 % 2 ==0:
    print("O número par.")
else:
    print("O número impar.")
