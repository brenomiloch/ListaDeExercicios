# Escreva um programa que calcule o valor total de uma compra, considerando o preço de um produto e a quantidade 
# comprada. O programa deve solicitar ao usuário o preço e a quantidade do produto e, em seguida, exibir o valor
# total da compra.
preco_produto = float(input("Digite o preço do produto: "))
quantidade_produto = int(input("Digite o preço do quantidade: "))
total = preco_produto * quantidade_produto
print("O valor total R$", total)