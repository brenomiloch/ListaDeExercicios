# EXEMPLO TABELA DA VERDADE DO OPERADOR | (OU).
#         A    |    B    
print("A:False | B:False = ",False | False)
print("A:False | B:True  = ",False | True)
print("A: True | B:False  = ",True | False)
print("A: True | B:True   = ",True | True)
# O operador "|" retorna True (verdadeiro) se pelo menos uma das expressões for verdadeira e retorna False (falso)
# apenas se ambas as expressões forem falsas.
