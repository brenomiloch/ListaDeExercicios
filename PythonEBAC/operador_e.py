# EXEMPLO TABELA DA VERDADE DO OPERADOR & (E).
#         A    |    B    
print("A:False &  B:False  = ",False & False)
print("A:False &  B:True   = ",False & True)
print("A: True &  B:False  = ",True & False)
print("A: True &  B:True   = ",True & True)
# O operador "&" retorna True (verdadeiro) somente se ambas as expressões forem verdadeiras. Caso contrário,ele retorna False (falso).
